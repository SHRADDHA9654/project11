
var img=document.querySelector(".img12");
img.addEventListener("mouseenter" , car);
img.addEventListener("mouseleave" , rac);



function car() {
   
   img.style.transform="rotate(720deg)" 
   img.style.transition="4s ease"
}

function rac(){
    img.style.transform="rotate(-720deg)" 
    img.style.transition="1s ease"
}

